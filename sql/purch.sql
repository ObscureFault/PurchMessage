CREATE TABLE IF NOT EXISTS `PURCHMSG` (
	`PURCH_ID` INT(20) NOT NULL AUTO_INCREMENT,

    `NAME` VARCHAR(200) NOT NULL CHARACTER SET utf8 COLLATE utf8_general_ci,
	`MESSAGE` VARCHAR(400) NOT NULL CHARACTER SET utf8 COLLATE utf8_general_ci,
	`RESPONSE` VARCHAR(400) NULL CHARACTER SET utf8 COLLATE utf8_general_ci,
	`PURCHASE_COUNT` INT(20) zerofill,
	`PURCHASE_SUMMARY` VARCHAR(400) NULL CHARACTER SET utf8 COLLATE utf8_general_ci, -- list of item names & purcheas counts

	`PURCHASE_IMAGE_URL` VARCHAR(200) NOT NULL CHARACTER SET utf8 COLLATE utf8_general_ci,

	`CREATED_STAMP` DATETIME(20) NOT NULL,
    `RESPONSE_STAMP` DATETIME(20) NULL,   -- when response was sumbitted
    `ONSCREEN_STAMP` DATETIME(20) NULL,   -- when response was sumbitted


	`CURATED_STAMP` DATETIME(20) NULL,   -- To show up in Curated filter list     unless RESPONSE_STAMP OR CURATED_COMPLETE_STAMP
	`CURATED_COMPLETE_STAMP` DATETIME(20) NULL,   -- Prevents from showing up in curated 

    UNIQUE KEY(`PURCH_ID`),
	FULLTEXT `idx_msg` (`MESSAGE`) USING BTREE,
	FULLTEXT `idx_psum` (`PURCHASE_SUMMARY`) USING BTREE,
    INDEX `idx_pcount` (`PURCHASE_COUNT`)
) ENGINE=MyISAM;